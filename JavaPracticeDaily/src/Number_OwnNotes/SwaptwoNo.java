package Number_OwnNotes;

public class SwaptwoNo {

	public static void main(String[] args) {
		int temp,a=10,b=20;
		System.out.println("Before swap");
		System.out.println(a);
		System.out.println(b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swap");
		System.out.println(a);
		System.out.println(b);

	}

}
